package Pattern;

public class Solid_rectangle {

	public static void main(String[] args) {

		/* Solid rectangle 5 column and 4 rows
		 * Pattern :1
		 *****
		 *****
		 *****
		 *****
		 */
		for (int i = 0; i <= 4; i++) {
			for (int j = 0; j <= 4; j++) {
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
